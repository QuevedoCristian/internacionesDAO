    private void jbAltaHabitacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAltaHabitacionActionPerformed
        habitacion = new Habitacion();

        habitacion.setNumero(Integer.parseInt(jtfNumero.getText()));
        habitacion.setUbicacion((Ubicacion) jcbUbicaciones.getSelectedItem());

        try {
            habitacionDAO.create(habitacion);
            limpiarCampos();
        } catch (Exception ex) {
            Logger.getLogger(AbmHabitacion.class.getName()).log(Level.SEVERE, null, ex);
        }

        try {
            jtListadoHabitaciones.setModel(new GrillaHabitacion(habitacionDAO.read()));
        } catch (Exception ex) {
            Logger.getLogger(AbmHabitacion.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jbAltaHabitacionActionPerformed

    private void jtListadoHabitacionesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtListadoHabitacionesMouseClicked
        // TODO add your handling code here:
        Habitacion habitacion = grillaHabitacion.getHabitacionFromRow(jtListadoHabitaciones.getSelectedRow());

        jtfId.setText(habitacion.getId().toString());
        jtfNumero.setText(habitacion.getNumero().toString());
        jcbUbicaciones.setSelectedItem(habitacion.getUbicacion());
    }//GEN-LAST:event_jtListadoHabitacionesMouseClicked


