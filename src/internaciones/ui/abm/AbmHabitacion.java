    private void jbModificarHabitacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbModificarHabitacionActionPerformed
        // TODO add your handling code here:
        habitacion = new Habitacion();

        habitacion.setNumero(Integer.parseInt(jtfNumero.getText()));
        habitacion.setUbicacion((Ubicacion) jcbUbicaciones.getSelectedItem());
        habitacion.setId(Integer.parseInt(jtfId.getText()));

        try {
            habitacionDAO.update(habitacion);
        } catch (Exception ex) {
            Logger.getLogger(AbmHabitacion.class.getName()).log(Level.SEVERE, null, ex);
        }

        try {
            jtListadoHabitaciones.setModel(new GrillaHabitacion(habitacionDAO.read()));
        } catch (Exception ex) {
            Logger.getLogger(AbmHabitacion.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jbModificarHabitacionActionPerformed

