    private void jtListadoInternacionesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtListadoInternacionesMouseClicked
        // TODO add your handling code here:
        Internacion internacion = grillaInternacion.getInternacionFromRow(jtListadoInternaciones.getSelectedRow());

        jtfId.setText(internacion.getId().toString());
        jtfPaciente.setText(internacion.getPaciente());
        
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy"); // Define el formato deseado
        String fechaTexto = dateFormat.format(internacion.getFecha());
        jtfFecha.setText(fechaTexto);
        
        jtfDiagnostico.setText(internacion.getDiagnostico());
        jcbCamas.setSelectedItem(internacion.getCama());
    }//GEN-LAST:event_jtListadoInternacionesMouseClicked

