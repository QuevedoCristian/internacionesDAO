    private void jbAltaInternacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAltaInternacionActionPerformed
        internacion = new Internacion();
        
        String fechaTexto = jtfFecha.getText();
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        try {
            Date fecha = dateFormat.parse(fechaTexto);
            internacion.setFecha(fecha);
        } catch (ParseException ex) {
            Logger.getLogger(AbmInternacion.class.getName()).log(Level.SEVERE, null, ex);
        }

        internacion.setPaciente(jtfPaciente.getText());
        internacion.setDiagnostico(jtfDiagnostico.getText());
        internacion.setCama((Cama) jcbCamas.getSelectedItem());

        try {
            internacionDAO.create(internacion);
            limpiarCampos();
        } catch (Exception ex) {
            Logger.getLogger(AbmInternacion.class.getName()).log(Level.SEVERE, null, ex);
        }

        try {
            jtListadoInternaciones.setModel(new GrillaInternacion(internacionDAO.read()));
        } catch (Exception ex) {
            Logger.getLogger(InternacionDAO.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jbAltaInternacionActionPerformed

