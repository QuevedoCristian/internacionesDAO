    private void jbModificarUbicacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbModificarUbicacionActionPerformed
        // TODO add your handling code here:
        ubicacion = new Ubicacion();
        
        ubicacion.setHospital(jtfHospital.getText());
        ubicacion.setNombre(jtfNombre.getText());
        ubicacion.setId(Integer.parseInt(jtfId.getText()));
        
        try {
            ubicacionDAO.update(ubicacion);
        } catch (Exception ex) {
            Logger.getLogger(AbmUbicacion.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {
            jtListadoUbicaciones.setModel(new GrillaUbicacion(ubicacionDAO.read()));
        } catch (Exception ex) {
            Logger.getLogger(AbmUbicacion.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jbModificarUbicacionActionPerformed

