    private void jbAltaUbicacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAltaUbicacionActionPerformed
        ubicacion = new Ubicacion();

        ubicacion.setHospital(jtfHospital.getText());
        ubicacion.setNombre(jtfNombre.getText());

        try {
            ubicacionDAO.create(ubicacion);
            limpiarCampos();
        } catch (Exception ex) {
            Logger.getLogger(AbmUbicacion.class.getName()).log(Level.SEVERE, null, ex);
        }

        try {
            jtListadoUbicaciones.setModel(new GrillaUbicacion(ubicacionDAO.read()));
        } catch (Exception ex) {
            Logger.getLogger(AbmUbicacion.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jbAltaUbicacionActionPerformed

    private void jtListadoUbicacionesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtListadoUbicacionesMouseClicked
        // TODO add your handling code here:
        Ubicacion ubicacion = grillaUbicacion.getUbicacionFromRow(jtListadoUbicaciones.getSelectedRow());

        jtfHospital.setText(ubicacion.getHospital());
        jtfNombre.setText(ubicacion.getNombre());
        jtfId.setText(ubicacion.getId().toString());

    }//GEN-LAST:event_jtListadoUbicacionesMouseClicked


